def area_triangle(base, height):
    """Given lengths base and height of a triangle,
    compute and return the triangle area."""
    area = 0.5 * base * height
    return area


def mysum(a, b):
    """
    Compute and return sum of a and b.

    Parameters
    ----------
    a : numeric
        Part of the sum.
    b : numeric
        Part of the sum.

    Returns
    -------

    numeric
        Sum of a and b.

    """
    return a + b
